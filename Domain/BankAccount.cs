namespace FinanceTracker.Domain;

/// <summary>
/// Банковский счёт в доменной модели.
/// <para>
/// <b>Id</b> — уникальный идентификатор (long).<br/>
/// <b>Name</b> — человекочитаемое название счёта (например, «Основной», «Накопительный»).<br/>
/// <b>Balance</b> — заявленный баланс счёта. Это значение может отличаться от
/// «рассчитанного» по операциям и используется в отчёте сверки (reconcile).
/// </para>
/// </summary>
/// <remarks>
/// Тип сделан как <c>record</c>, чтобы удобно передавать неизменяемые срезы данных
/// и использовать «with»-копирование при частичном обновлении (см. команды Edit*).
/// </remarks>
public record BankAccount(long Id, string Name, double Balance);